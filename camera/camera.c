//-------------------------------------------------------------------------------------------------------------------
//  @brief      MT9V032摄像头初始化
//  @param      NULL
//  @return     void					
//  @since      v1.0
//  Sample usage:		
//-------------------------------------------------------------------------------------------------------------------
void Camera_Init(void)
{
    //设置参数    具体请参看使用手册
    uint16 temp, i;
    uint8  send_buffer[4];
    
    uart_init (MT9V032_COF_UART, 9600);	//初始换串口 配置摄像头    
    uart_rx_irq_en(MT9V032_COF_UART);
    
    //等待摄像头上电初始化成功
    systick_delay_ms(1000);
    uart_receive_flag = 0;
    
    //开始配置摄像头并重新初始化
    for(i=0; i<CONFIG_FINISH; i++)
    {
        send_buffer[0] = 0xA5;
        send_buffer[1] = MT9V032_CFG[i][0];
        temp = MT9V032_CFG[i][1];
        send_buffer[2] = temp>>8;
        send_buffer[3] = (uint8)temp;
        
        uart_putbuff(MT9V032_COF_UART,send_buffer,4);
        systick_delay_ms(2);
    }
    //等待摄像头初始化成功
    while(!uart_receive_flag);
    uart_receive_flag = 0;
    while((0xff != receive[1]) || (0xff != receive[2]));
    //以上部分对摄像头配置的数据全部都会保存在摄像头上51单片机的eeprom中
    //利用set_exposure_time函数单独配置的曝光数据不存储在eeprom中
    
    //获取配置便于查看配置是否正确
    get_config();
    
    //摄像头采集初始化
    DisableInterrupts;
    //DMA通道0初始化，PTC18触发源(默认上升沿)，源地址为C_IN_DATA(1)(PTC8-PTC15)，目的地址为：image，每次传输1Byte 传输完毕保持目的地址
    dma_portx2buff_init(DMA_CH0, (void *)&C_IN_DATA(1), (void *)Image1, C18, DMA_BYTE1, 2*COLUMN*2*ROW, DADDR_KEEPON);
    port_init(C18, ALT1 | DMA_RISING | PULLDOWN);  			    //PCLK  触发源设置
    DMA_DIS(DMA_CH0);                                     		//禁用DMA通道
    DMA_IRQ_CLEAN(DMA_CH0);                               		//清除通道传输中断标志位
    DMA_IRQ_EN(DMA_CH0);                                  		//允许DMA通道中断
    DMA_EN(DMA_CH0);  											//使能DMA
    
    disable_irq(PORTC_IRQn);                             		//关闭PTC的中断
    port_init(C19, ALT1 | IRQ_FALLING | PULLDOWN);        		//场中断，下降沿触发中断、下拉
    set_irq_priority(PORTC_IRQn,1);                             //中断优先级
    
    Image_Tran = 1;
    Image_Lock = 0;
}

void Camera_Extract(void)
{
    uint8 i,j;
    if(Image_Tran == 1 && Image_Lock != 2)
    {
        for(i=0;i<ROW*2;i+=2)
        {
//            memcpy(*(Image + i/2),*(Image2 + i), sizeof(*(Image2 + i)));
          for(j=0;j<COLUMN*2;j+=2)
          {
            Image[i/2][j/2] = Image2[i][j];
          }
        }
        Image_Lock = 2;
    }
    else if(Image_Tran == 2 && Image_Lock != 1)
    {
        for(i=0;i<ROW*2;i+=2)
        {           
//            memcpy(*(Image + i/2),*(Image1 + i), sizeof(*(Image1 + i)));
          for(j=0;j<COLUMN*2;j+=2)
          {
           Image[i/2][j/2] = Image1[i][j]; 
          }
        }
        Image_Lock = 1;
    }
}

//-------------------------------------------------------------------------------------------------------------------
//  @brief      MT9V032摄像头场中断
//  @param      NULL
//  @return     void			
//  @since      v1.0
//  Sample usage:				在isr.c里面先创建对应的中断函数，然后调用该函数(之后别忘记清除中断标志位)
//-------------------------------------------------------------------------------------------------------------------
void Camera_ISR(void)
{
    DMA_DIS(DMA_CH0);
    
    if(Image_Tran == 1)
        Image_Tran = 2;  //切换传图，图1图2依次传输
    else if(Image_Tran == 2)
        Image_Tran = 1;
    
    if(Image_Tran == 1)//将要传图1且图1没有在处理，或者将要传图2且图2没有在处理
        dma_repeat(DMA_CH0,(void *)&C_IN_DATA(1),(void *)Image2[0],COLUMN*ROW*4);
    else if(Image_Tran == 2)
        dma_repeat(DMA_CH0,(void *)&C_IN_DATA(1),(void *)Image1[0],COLUMN*ROW*4);
    
    DMA_EN(DMA_CH0);
}

